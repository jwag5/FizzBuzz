'''
  fizzBuzz
    low - the starting number to output
    high - the last number to be output
    
    This function will output values from low to high replacing
    multiples of 3 with "Fizz", multiples of 5 with "Buzz" and
    multiples of both with "FizzBuzz". It will do so recursively,
    so as to not use any loops as per instructions.
'''
def fizzBuzz(low, high):
  if low > high:
    return
  
  if low % 3 == 0 and low % 5 == 0:
    print("FizzBuzz")
  elif low % 3 == 0:
    print("Fizz")
  elif low % 5 == 0:
    print("Buzz")
  else:
    print(low)
    
  low += 1
  
  fizzBuzz(low, high)
#end fizzBuzz  
  

#Testing fizzBuzz
#Get user input
low = int(input("Enter lower bound number: "))
high = int(input("Enter upper bound number: "))
#Call function
fizzBuzz(low, high)
